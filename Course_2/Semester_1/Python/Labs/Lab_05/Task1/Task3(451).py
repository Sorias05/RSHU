# Напишіть програму, яка зчитує числа (по одному в рядку) до тих пір, поки сума введених чисел не буде дорівнює 0 і відразу після цього виводить суму квадратів всіх чисел. Гарантується, що в якийсь момент сума введених чисел дорівнюватиме 0, після цього зчитування продовжувати не потрібно.

x = int(input("Enter number: "))
res = x
squares = x*x
while(res != 0):
    x = int(input("Enter number: "))
    res += x
    squares += x*x
print(squares)
