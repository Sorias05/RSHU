# Програма отримує на вхід послідовність цілих невід’ємних чисел, кожне число вводиться в окремому рядку. Послідовність завершується числом 0, при зчитуванні якого програма повинна закінчити свою роботу і вивести кількість членів послідовності (не рахуючи завершального числа 0).

n = int(input("Input number: "))
count = 0
while n != 0:
    n = int(input("Input number: "))
    count += 1
print(count)
